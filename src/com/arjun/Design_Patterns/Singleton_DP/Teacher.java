package com.arjun.Design_Patterns.Singleton_DP;

import java.util.Objects;

public class Teacher {

	private int teacherid;
	private String teachername;
	private String teacherdept;
	private int teacherdeptcode;
	
	private String teacheraddress;

	public int getTeacherid() {
		return teacherid;
	}

	public void setTeacherid(int teacherid) {
		this.teacherid = teacherid;
	}

	public String getTeachername() {
		return teachername;
	}

	public void setTeachername(String teachername) {
		this.teachername = teachername;
	}

	public String getTeacherdept() {
		return teacherdept;
	}

	public void setTeacherdept(String teacherdept) {
		this.teacherdept = teacherdept;
	}

	public int getTeacherdeptcode() {
		return teacherdeptcode;
	}

	public void setTeacherdeptcode(int teacherdeptcode) {
		this.teacherdeptcode = teacherdeptcode;
	}

	public String getTeacheraddress() {
		return teacheraddress;
	}

	public void setTeacheraddress(String teacheraddress) {
		this.teacheraddress = teacheraddress;
	}

	

	
	
	
	
}
