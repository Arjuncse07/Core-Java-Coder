package object.oriented.programming;

public class ContactInfo {
private int mobileNum;
private int landLineNum;
private int emergencyContactNum;
private int residentialAddr;
public int getMobileNum() {
	return mobileNum;
}
public void setMobileNum(int mobileNum) {
	this.mobileNum = mobileNum;
}
public int getLandLineNum() {
	return landLineNum;
}
public void setLandLineNum(int landLineNum) {
	this.landLineNum = landLineNum;
}
public int getEmergencyContactNum() {
	return emergencyContactNum;
}
public void setEmergencyContactNum(int emergencyContactNum) {
	this.emergencyContactNum = emergencyContactNum;
}
public int getResidentialAddr() {
	return residentialAddr;
}
public void setResidentialAddr(int residentialAddr) {
	this.residentialAddr = residentialAddr;
}

}
